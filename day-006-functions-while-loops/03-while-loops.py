# While loops are used until something is no longer true.
# For example:

number_of_hurdles = 6

# Loop through the actions in the loop until number_of_hurdles is 0.
# Once it is at 0, the loop will stop as it is no longer true.
while number_of_hurdles > 0:
    print(number_of_hurdles)
    # Decrease the value of number_of_hurdles by 1 each time it loops through.
    number_of_hurdles -= 1

