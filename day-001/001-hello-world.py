# Output "Hello World!!" to the console
print("Hello World!!")

# If you need to use quotations within a print output of a string, there are two options you can do:

# 1. Use different quote marks as python will recognise the difference:
print("An example of 'a string'")
# or
print('An example of "a string"')

# 2. You can use the \ as an escape charecter to get around python getting muddled up:
print("An example of \"a string\"")

# Print text over multiple lines (use \n)
print("Hello World!!\nHello World!!")

# Concatenate a number of strings together
print("Hello "+"World!!")
# Or
print("Hello"+" "+"World!!")
